#include<stdio.h>

int main(){
    //increment operator
    // ++i - pe increment
    // i++ - post operator
    int i=0;
    printf("%d \n", i++); //use, then increase
    printf("%d \n", i); //incremented value
 
    printf("%d \n", ++i); //increase, then use
    printf("%d \n", i); //incremented value

}